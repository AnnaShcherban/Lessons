//Знайти кількість років

//Описуємо і вводимо значення зміних
let happyBirthay = parseFloat(prompt("Введіть дату свого народження", "0"))
const CURRENR_YEAR = 2024

//Знаходимо результат
let year_Birth = CURRENR_YEAR - happyBirthay

//Виводимо результат
document.write(`Кількість років=${year_Birth}`)
